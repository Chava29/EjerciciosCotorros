package fors;

public class Ejercicio3
{
    /*
    Programa que imprima desde el 200 hasta el 250 de dos en dos
     */

    public static void main(String[] args)
    {
        System.out.println("Ingresa un numero quieras, va a ir de 2 en 2");
        for (int i = 200; i<=250; i+=2)
        {
            System.out.println(i);
        }
    }
}
